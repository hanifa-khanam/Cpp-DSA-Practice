#include <iostream>
using namespace std;

int main() {

    // Arithmetic Binary operators
    int a = 5;
    int b = 3;

    cout << "+ " << (a + b) << endl;
    cout << "- " << (a - b) << endl;
    cout << "* " << (a * b) << endl;
    cout << "/ " << (a / b) << endl;
    cout << "% " << (a % b) << endl;

    return 0;    
}